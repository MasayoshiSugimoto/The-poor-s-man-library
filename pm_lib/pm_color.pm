package pm_color;
use strict;
use warnings;


# Define ANSI escape sequences for basic colors
our $RESET  = "\e[0m";
our $RED    = "\e[31m";
our $GREEN  = "\e[32m";
our $YELLOW = "\e[33m";
our $BLUE   = "\e[34m";
our $MAGENTA= "\e[35m";
our $CYAN   = "\e[36m";
our $WHITE  = "\e[37m";
our $BLACK  = "\e[30m";
our $GREY   = "\e[90m";


1;
